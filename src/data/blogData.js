// Fonction pour récupérer un article par son slug
export const getBlogPostBySlug = (slug) => {
  return blogPosts.find(post => post.slug === slug);
};

// Fonction pour récupérer un article par son ID
export const getBlogPostById = (id) => {
  return blogPosts.find(post => post.id === id);
};

// Fonction pour récupérer les articles par catégorie
export const getBlogPostsByCategory = (category) => {
  return blogPosts.filter(post => post.category === category);
};

// Fonction pour récupérer les articles par tag
export const getBlogPostsByTag = (tag) => {
  return blogPosts.filter(post => post.tags.includes(tag));
};

// Fonction pour récupérer les articles les plus récents
export const getRecentBlogPosts = (count = 3) => {
  return [...blogPosts]
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, count);
};

// Fonction pour récupérer toutes les catégories uniques
export const getAllCategories = () => {
  const categories = blogPosts.map(post => post.category);
  return [...new Set(categories)];
};

// Fonction pour récupérer tous les tags uniques
export const getAllTags = () => {
  const tags = blogPosts.flatMap(post => post.tags);
  return [...new Set(tags)];
};
